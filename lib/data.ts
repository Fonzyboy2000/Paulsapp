// Helper functions
export function getHospitalById(id: string) {
  return hospitals.find((h) => h.id === id)
}

export function getDoctorById(id: string) {
  return doctors.find((d) => d.id === id)
}

export function getOperationById(id: string) {
  return operations.find((o) => o.id === id)
}

export function getDoctorsByHospital(hospitalId: string) {
  const hospital = getHospitalById(hospitalId)
  if (!hospital) return []
  return doctors.filter((d) => hospital.doctors.includes(d.id))
}

export function getOperationsByDoctor(doctorId: string) {
  const doctor = getDoctorById(doctorId)
  if (!doctor) return []
  return operations.filter((o) => doctor.operations.includes(o.id))
}

export function getProductById(id: string) {
  return products.find((p) => p.id === id)
}

// Search function
export function searchAll(query: string) {
  const q = query.toLowerCase()

  const matchedDoctors = doctors.filter(
    (d) => d.name.toLowerCase().includes(q) || d.specialty.toLowerCase().includes(q),
  )

  const matchedProducts = products.filter(
    (p) => p.name.toLowerCase().includes(q) || p.category.toLowerCase().includes(q) || p.sku.toLowerCase().includes(q),
  )

  const matchedCompetitors = competitorProducts.filter(
    (c) =>
      c.competitor.toLowerCase().includes(q) ||
      c.productName.toLowerCase().includes(q) ||
      c.ourEquivalent.toLowerCase().includes(q),
  )

  const matchedHospitals = hospitals.filter((h) => h.name.toLowerCase().includes(q) || h.city.toLowerCase().includes(q))

  const matchedOperations = operations.filter(
    (o) => o.name.toLowerCase().includes(q) || o.description.toLowerCase().includes(q),
  )

  return { matchedDoctors, matchedProducts, matchedCompetitors, matchedHospitals, matchedOperations }
}

