// Data for the medical field rep app

export interface Hospital {
  id: string
  name: string
  code: string
  city: string
  address: string
  phone: string
  website: string
  healthAuthority: string
  hasSurgicalWorkflows: boolean
  doctors: string[] // doctor IDs
}

export interface Doctor {
  id: string
  name: string
  specialty: string
  email: string
  phone: string
  address: string
  imageUrl: string
  hospitalIds: string[]
  preferences: string[]
  notes: string
  lastVisit: string
  operations: string[] // operation IDs
}

export interface Operation {
  id: string
  name: string
  category: "joint" | "trauma" | "spine"
  description: string
  steps: OperationStep[]
  estimatedDuration: string
  productsUsed: string[]
}

export interface StepAttachment {
  id: string
  name: string
  type: "document" | "image" | "pdf" | "word"
  url: string
}

export interface StepSet {
  id: string
  description: string
  setsNumber: string
}

export interface OperationStep {
  order: number
  description: string
  products: string[]
  duration: string
  notes?: string
  doctorPreferences?: string
  attachments?: StepAttachment[]
  sets?: StepSet[]
}

export interface Product {
  id: string
  name: string
  category: string
  sku: string
  description: string
  specifications: Record<string, string>
  compatibleWith: string[]
  imageUrl: string
}

export interface CompetitorProduct {
  id: string
  competitor: string
  productName: string
  ourEquivalent: string
  advantages: string[]
  disadvantages: string[]
  keyTalkingPoints: string[]
}

export interface CallNote {
  id: string
  doctorId: string
  date: string
  notes: string
  createdBy?: string
}

